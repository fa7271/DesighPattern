package Adapter;

public class Math {
    // 요구사항이 float 이라 안됨
    public static double twotime(double num) {
        return num*2;
    }
    public static double half(double num) {
        return num/2;
    }

    //강화된 알고리즘
    public static Double doubled(Double d) {
        return d*2 ;
    }
}
